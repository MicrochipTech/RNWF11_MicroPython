/******************************************************************************
  Function:
    void APP_Tasks ( void )

  Remarks:
    See prototype in app.h.
 */

void APP_SST_Tasks ( void )
{
    /* Check the application's current state. */
    switch ( appsstData.state )
    {
        case APP_SST_STATE_INIT:
        {
            appsstData.state = APP_STATE_RESET_FLASH;
        }

        case APP_STATE_RESET_FLASH:
        {
            if (APP_ResetFlash() != APP_TRANSFER_COMPLETED)
            {
                appsstData.state = APP_STATE_ERROR;
            }
            else
            {
                appsstData.state = APP_STATE_ENABLE_QUAD_IO;
            }
            break;
        }

        case APP_STATE_ENABLE_QUAD_IO:
        {
            if (APP_EnableQuadIO() != APP_TRANSFER_COMPLETED)
            {
                appsstData.state = APP_STATE_ERROR;
            }
            else
            {
                appsstData.state = APP_STATE_UNLOCK_FLASH;
            }
            break;
        }

        case APP_STATE_UNLOCK_FLASH:
        {
            if (APP_UnlockFlash() != APP_TRANSFER_COMPLETED)
            {
                appsstData.state = APP_STATE_ERROR;
            }
            else
            {
                appsstData.state = APP_STATE_READ_JEDEC_ID;
            }
            break;
        }

        case APP_STATE_READ_JEDEC_ID:
        {
            if (APP_ReadJedecId(&appsstData.jedec_id) != APP_TRANSFER_COMPLETED)
            {
                appsstData.state = APP_STATE_ERROR;
                break;
            }

            if (appsstData.jedec_id != SST26VF064B_JEDEC_ID)
            {
                appsstData.state = APP_STATE_ERROR;
                break;
            }

            appsstData.state = APP_STATE_ERASE_FLASH;

            break;
        }

        case APP_STATE_ERASE_FLASH:
        {
            if (APP_SectorErase((MEM_START_ADDRESS + sector_index)) != APP_TRANSFER_COMPLETED)
            {
                appsstData.state = APP_STATE_ERROR;
                break;
            }

            appsstData.state = APP_STATE_ERASE_WAIT;

            break;
        }

        case APP_STATE_ERASE_WAIT:
        {
            if (APP_TransferStatusCheck() == APP_TRANSFER_COMPLETED)
            {
                sector_index += SECTOR_SIZE;

                if (sector_index < BUFFER_SIZE_SST)
                {
                    appsstData.state = APP_STATE_ERASE_FLASH;
                }
                else
                {
                    appsstData.state = APP_STATE_WRITE_MEMORY;
                }
            }
            break;
        }

        case APP_STATE_WRITE_MEMORY:
        {
         // if (APP_MemoryWrite((uint32_t *)&appsstData.writeBuffer[write_index], PAGE_SIZE , (MEM_START_ADDRESS + write_index)) != APP_TRANSFER_COMPLETED)
          if (APP_MemoryWrite((uint32_t *)&diskImage_test[write_index], PAGE_SIZE, (MEM_START_ADDRESS + write_index)) != APP_TRANSFER_COMPLETED)
            {
                appsstData.state = APP_STATE_ERROR;
                break;
            }

            appsstData.state = APP_STATE_WRITE_WAIT;

            break;
        }

        case APP_STATE_WRITE_WAIT:
        {
            if (APP_TransferStatusCheck() == APP_TRANSFER_COMPLETED)
            {
                write_index += PAGE_SIZE;
                if (write_index < BUFFER_SIZE_SST)
                {
                    appsstData.state = APP_STATE_WRITE_MEMORY;
                }
                else
                {
                    appsstData.state = APP_STATE_READ_MEMORY;
                }
            }
            break;
        }

        case APP_STATE_READ_MEMORY:
        {
            if (APP_MemoryRead((uint32_t *)&appsstData.readBuffer[0],TEST_SIZE , MEM_START_ADDRESS) != APP_TRANSFER_COMPLETED)
            {
                appsstData.state = APP_STATE_ERROR;
                break;
            }

            appsstData.state = APP_STATE_VERIFY_DATA;

            break;
        }

        case APP_STATE_VERIFY_DATA:
        {
            if (!memcmp(diskImage_test, appsstData.readBuffer, TEST_SIZE))
            {
                appsstData.state = APP_STATE_SUCCESS;
            }
            else
            {
                appsstData.state = APP_STATE_ERROR;
            }

            break;
        }

        case APP_STATE_SUCCESS:
        {
            vTaskDelay(1000);
            LED_Toggle();

            break;
        }

        case APP_STATE_ERROR:
        default:
        {          
            
            LED_On();
            break;
        }
    }
}

